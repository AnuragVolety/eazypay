package com.example.vanur.ocrnumberplatedetection.User.Activity;

public class FireModel {

    public String tollName;
    public String transId;
    public String date;
    public String xAmount;

    public void setTollName(String tollName) {
        this.tollName = tollName;
    }

    public String getTollName() {
        return tollName;
    }

    public void setTransId(String transId) {
        this.transId = transId;
    }

    public String getTransId() {
        return transId;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDate() {
        return date;
    }

    public void setxAmount(String amount) {
        this.xAmount = amount;
    }

    public String getxAmount() {
        return xAmount;
    }
}
